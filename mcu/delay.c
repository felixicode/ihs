/********************************************************************
ms级延时函数delay_ms(u16 t)，有形参t，无返回值
********************************************************************/
void delay_ms(u16 ms)
{
	u16 i,j;
	for(i=ms;i>0;i--)
		for(j=110;j>0;j--);
}

void delay_10us(u16 ten_us)
{
	while(ten_us--);	
}

void delay_s(u16 n)
{
	while (n--)
		delay_ms(1000);
}
